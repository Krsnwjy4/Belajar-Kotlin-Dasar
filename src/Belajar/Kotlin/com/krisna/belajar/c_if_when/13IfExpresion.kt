fun main() {
    val puasa = 30

    if (puasa >= 30){
        println("SELAMAT HARI RAYA")
    } else if(puasa >= 15){
        println("PERBAIKI LAGI TAHUN DEPAN")
    } else if (puasa >= 5) {
        println("COBA LAGI TAHUN DEPAN")
    }else {
        println("MASUK NERAKA JALUR BEASISWA")
    }
}